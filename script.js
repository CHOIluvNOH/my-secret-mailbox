const mainView = document.querySelector("#mainView");
const loginView = document.querySelector("#loginView");
const letterView = document.querySelector("#letterView");
const mailboxButton = document.querySelector("#mailboxButton");
const loginForm = document.querySelector("#loginForm");
const nameInput = document.querySelector("#nameInput");
const passwordInput = document.querySelector("#passwordInput");
const errorMessage = document.querySelector("#errorMessage");
const letterContent = document.querySelector("#letterContent");
const recipientBadge = document.querySelector("#recipientBadge");
const backButton = document.querySelector("#backButton");

const views = [mainView, loginView, letterView];
const typingDelay = 34;
let typingTimer = null;

function showView(targetView) {
  views.forEach((view) => {
    view.classList.toggle("is-active", view === targetView);
  });
}

function hideError() {
  errorMessage.classList.remove("is-visible");
}

function showError() {
  errorMessage.classList.add("is-visible");
}

function clearTyping() {
  window.clearTimeout(typingTimer);
  typingTimer = null;
  letterContent.classList.remove("typing");
  letterContent.innerHTML = "";
}

function appendLetterImage(friend) {
  if (!friend.image) {
    return;
  }

  const figure = document.createElement("figure");
  figure.className = "letter-art";

  const image = document.createElement("img");
  image.src = friend.image;
  image.alt = friend.imageAlt || "편지에 첨부된 그림";

  const caption = document.createElement("figcaption");
  caption.textContent = friend.imageCaption || "ATTACHED SKETCH";

  figure.append(image, caption);
  letterContent.appendChild(figure);
}

function typeLetter(friend) {
  clearTyping();

  const text = friend.content;
  let index = 0;
  letterContent.classList.add("typing");

  function typeNextCharacter() {
    if (index >= text.length) {
      letterContent.classList.remove("typing");
      appendLetterImage(friend);
      return;
    }

    const character = text[index];

    if (character === "\n") {
      letterContent.appendChild(document.createElement("br"));
    } else {
      letterContent.appendChild(document.createTextNode(character));
    }

    index += 1;
    typingTimer = window.setTimeout(typeNextCharacter, typingDelay);
  }

  typeNextCharacter();
}

function openLoginView() {
  mailboxButton.classList.add("is-opening");

  window.setTimeout(() => {
    mailboxButton.classList.remove("is-opening");
    showView(loginView);
    nameInput.focus();
  }, 800);
}

function handleLogin(event) {
  event.preventDefault();

  const name = nameInput.value.trim();
  const password = passwordInput.value;
  const friend = friends[name];

  if (!friend || friend.password !== password) {
    showError();
    passwordInput.value = "";
    passwordInput.focus();
    return;
  }

  hideError();
  recipientBadge.textContent = `TO. ${name}`;
  showView(letterView);
  typeLetter(friend);
}

function resetMailbox() {
  nameInput.value = "";
  passwordInput.value = "";
  hideError();
  clearTyping();
  recipientBadge.textContent = "TO. FRIEND";
  showView(mainView);
  mailboxButton.focus();
}

mailboxButton.addEventListener("click", openLoginView);
loginForm.addEventListener("submit", handleLogin);
backButton.addEventListener("click", resetMailbox);
